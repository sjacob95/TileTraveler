/**
 *  An enum identifying whether to allow or disallow a series of Tiles
 *
 *  @author Jacob Stenzel
 *  @version Oct 31, 2013
 */
public enum PathSetting
{
    /**
     * Only allow the provided Tiles
     */
    ALLOW,
    /**
     * Only disallow the provided Tiles
     */
    DISALLOW
}
